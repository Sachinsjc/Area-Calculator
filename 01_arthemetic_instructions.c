#include <stdio.h>

int main()
{
    int a = 2;
    int b = 8;
    int c = 10;

    printf("The value for a+b+c is %d\n", a + b + c);
    printf("The value for a+b-c is %d\n", a + b - c);
    printf("The value for a*b*c is %d\n", a * b * c);
    printf("The value for a+b/c is %d\n", a + b / c);
    return 0;
}